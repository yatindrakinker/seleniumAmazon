package createdshopifyproductpojo;

public class ShopifyProduct {

	private String name;
	private String price;
	private String sku;
	private String barcode;
	private String inventory;
	private String productUrl;
	private String updatedName;
	private String updatedPrice;
	private String updatedSku;
	private String updatedBarcode;
	private String updatedInventory;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getSku() {
		return sku;
	}

	public void setSku(String sku) {
		this.sku = sku;
	}

	public String getBarcode() {
		return barcode;
	}

	public void setBarcode(String barcode) {
		this.barcode = barcode;
	}

	public String getInventory() {
		return inventory;
	}

	public void setInventory(String inventory) {
		this.inventory = inventory;
	}

	public String getProductUrl() {
		return productUrl;
	}

	public void setProductUrl(String productUrl) {
		this.productUrl = productUrl;
	}

	public String getUpdatedName() {
		return updatedName;
	}

	public void setUpdatedName(String updatedName) {
		this.updatedName = updatedName;
	}

	public String getUpdatedPrice() {
		return updatedPrice;
	}

	public void setUpdatedPrice(String updatedPrice) {
		this.updatedPrice = updatedPrice;
	}

	public String getUpdatedSku() {
		return updatedSku;
	}

	public void setUpdatedSku(String updatedSku) {
		this.updatedSku = updatedSku;
	}

	public String getUpdatedBarcode() {
		return updatedBarcode;
	}

	public void setUpdatedBarcode(String updatedBarcode) {
		this.updatedBarcode = updatedBarcode;
	}

	public String getUpdatedInventory() {
		return updatedInventory;
	}

	public void setUpdatedInventory(String updatedInventory) {
		this.updatedInventory = updatedInventory;
	}
}
